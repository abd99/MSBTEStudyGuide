public class SubItem
{

    String subName;
    String subCode;

    public SubItem(String subName, String subCode) {
        this.subName = subName;
        this.subCode = subCode;
    }

    public String getSubName() {
        return subName;
    }

    public String getSubCode() {
        return subCode;
    }
}
